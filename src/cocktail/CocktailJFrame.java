    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // Add Fruit:
        try {
            String fruitNmae = fruitNameTextField.getText();
            int fruitVolume = Integer.parseInt(fruitVolumeTextField.getText());
            int fruitCalories = Integer.parseInt(fruitCaloriesTextField.getText());

            int r = Integer.parseInt(rTextField.getText());
            int g = Integer.parseInt(gTextField.getText());
            int b = Integer.parseInt(bTextField.getText());
            if (r > 250 || g > 250 || b > 250 || r < 0 || g < 0 || b < 0) {
                throw new ColorOutOfRangeException();
            }
            Fruits fruit = new Fruits(fruitNmae, fruitCalories, fruitVolume, new Color(r, g, b));

            this.blender.add(fruit);
            this.ss.add(fruit);

        } catch (BlenderOverFlowException | NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        } catch (ColorOutOfRangeException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }

        //reset
        fruitNameTextField.setText("");
        fruitVolumeTextField.setText("");
        fruitCaloriesTextField.setText("");
        rTextField.setText("");
        gTextField.setText("");
        bTextField.setText("");
    }//GEN-LAST:event_jButton5ActionPerformed

