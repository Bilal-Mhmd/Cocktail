    public void saveObject() {
        try {
            ObjectOutputStream oos
                    = new ObjectOutputStream(new FileOutputStream("ObjectsFile.dat"));
            oos.writeObject(ss);
            oos.close();
        } catch (NotSerializableException ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage() + " is not serializable");
        } catch (IOException ex) {
            ex.getMessage();
        }

    }

