    private void openObjectsButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_openObjectsButtonActionPerformed
        // Open File of Objects
        String foi = "";
        ArrayList<Object> objectsList = readObjects();
        for (Object o : objectsList) {
            if (o instanceof Blender) {
                Blender b = (Blender) o;
                foi += b.getInfo() + "\n";
            } else if (o instanceof Fruits) {
                Fruits f = (Fruits) o;
                foi += f.getInfo() + "\n";
            } else if (o instanceof Milk) {
                Milk m = (Milk) o;
                foi += m.getInfo() + "\n";
            } else if (o instanceof Sugar) {
                Sugar s = (Sugar) o;
                foi += s.getInfo() + "\n";
            } else if (o instanceof Cup) {
                Cup c = (Cup) o;
                foi += c.getInfo() + "\n";
            }
        }

        JOptionPane.showMessageDialog(null, foi);
    }//GEN-LAST:event_openObjectsButtonActionPerformed

